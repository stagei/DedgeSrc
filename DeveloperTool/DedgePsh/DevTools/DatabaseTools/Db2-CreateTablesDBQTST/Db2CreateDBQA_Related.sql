-- =============================================
-- View Usage Analysis Views
-- =============================================
-- Views for analyzing database view usage patterns
CREATE VIEW TV.V_DBQA_VIEWS_NOT_ACCESSED_INTERNALLY AS
WITH VIEWS_USAGE AS (
    -- Get all views from consumed objects
    SELECT DISTINCT 
        REFERENCE_OBJECT_SCHEMA_NAME AS VIEW_SCHEMA,
        REFERENCE_OBJECT_NAME AS VIEW_NAME
    FROM DBM.DBQA_CONSUMED_OBJECTS
    WHERE REFERENCE_OBJECT_TYPE = 'VIEW'
),
ALL_VIEWS AS (
    -- Get all views from system catalog
    SELECT 
        VIEWSCHEMA AS VIEW_SCHEMA,
        VIEWNAME AS VIEW_NAME
    FROM SYSCAT.VIEWS
    WHERE VIEWSCHEMA NOT IN ('SYSCAT', 'SYSIBM', 'SYSSTAT', 'SYSFUN', 'SYSIBMADM')
)
SELECT 
    av.VIEW_SCHEMA,
    av.VIEW_NAME,
    'This view is not referenced in any analyzed SQL' as STATUS
FROM ALL_VIEWS av
LEFT JOIN VIEWS_USAGE vu 
    ON av.VIEW_SCHEMA = vu.VIEW_SCHEMA 
    AND av.VIEW_NAME = vu.VIEW_NAME
WHERE vu.VIEW_NAME IS NULL
AND av.VIEW_SCHEMA IN (SELECT SCHEMA_NAME FROM TV.V_DBQA_SCHEMA_NAME);

COMMENT ON TABLE TV.V_DBQA_VIEWS_NOT_ACCESSED_INTERNALLY IS 'Identifies database views that are not referenced in any analyzed SQL statements, helping identify potentially unused views';
GRANT SELECT ON TV.V_DBQA_VIEWS_NOT_ACCESSED_INTERNALLY TO PUBLIC;
GRANT CONTROL ON TABLE TV.V_DBQA_VIEWS_NOT_ACCESSED_INTERNALLY TO FKGEISTA;
GRANT CONTROL ON TABLE TV.V_DBQA_VIEWS_NOT_ACCESSED_INTERNALLY TO FKSVEERI;
COMMIT;

