-- =============================================
-- Package Usage Analysis Views
-- =============================================
-- Views for analyzing package usage patterns
CREATE OR REPLACE VIEW TV.V_UNUSED_PACKAGES_ANALYSIS AS
WITH PACKAGE_TABLES AS (
    -- Get all tables referenced by each package through DBQA tables
    SELECT DISTINCT 
        dsa.SOURCE_SCHEMA_NAME AS PACKAGE_SCHEMA_NAME,
        dsa.SOURCE_NAME AS PACKAGE_NAME,
        dco.REFERENCE_OBJECT_SCHEMA_NAME AS TABLE_SCHEMA,
        dco.REFERENCE_OBJECT_NAME AS TABLE_NAME
    FROM DBM.DBQA_SQL_ANALYSIS dsa
    JOIN DBM.DBQA_STATEMENT_PARTS dsp ON dsa.DBQA_SQL_ANALYSIS_ID = dsp.DBQA_SQL_ANALYSIS_ID
    JOIN DBM.DBQA_CONSUMED_OBJECTS dco ON dsp.DBQA_STATEMENT_PARTS_ID = dco.DBQA_STATEMENT_PARTS_ID
    WHERE dco.REFERENCE_OBJECT_TYPE = 'TABLE'
    AND dsa.OBJECT_TYPE = 'PACKAGE'
),
TABLE_USAGE AS (
    -- Get last usage information for tables
    SELECT 
        PACKAGE_SCHEMA_NAME,
        PT.PACKAGE_NAME,
        PT.TABLE_SCHEMA,
        PT.TABLE_NAME,
        T.LASTUSED
    FROM PACKAGE_TABLES PT
    JOIN SYSCAT.TABLES T 
        ON PT.TABLE_SCHEMA = T.TABSCHEMA 
        AND PT.TABLE_NAME = T.TABNAME
),
PACKAGE_LAST_USAGE AS (
    -- Get the most recent LASTUSED date for each package's tables
    SELECT 
        PACKAGE_SCHEMA_NAME,
        PACKAGE_NAME,
        MAX(LASTUSED) AS MOST_RECENT_TABLE_USAGE
    FROM TABLE_USAGE
    GROUP BY PACKAGE_SCHEMA_NAME, PACKAGE_NAME
)
SELECT 
    p.PACKAGE_SCHEMA_NAME,
    p.PACKAGE_NAME,
    p.MOST_RECENT_TABLE_USAGE,
    CASE 
        WHEN p.MOST_RECENT_TABLE_USAGE IS NULL THEN 'No usage data available'
        ELSE 'Last table usage was ' || CAST(DAY(CURRENT DATE - DATE(p.MOST_RECENT_TABLE_USAGE)) AS VARCHAR(10)) || ' days ago'
    END AS STATUS,
    pse.PROGRAM_IN_USE,
    pse.COMMENT,
    pse.USED_BY,
    'Unused based on table access' as DETECTION_METHOD,
    CASE WHEN p.MOST_RECENT_TABLE_USAGE IS NULL THEN 1 ELSE 2 END as SORT_NULL_FIRST,
    COALESCE(p.MOST_RECENT_TABLE_USAGE, DATE('9999-12-31')) as SORT_DATE
FROM PACKAGE_LAST_USAGE p
LEFT JOIN DBM.DBQA_PACKAGE_SOURCE_EXISTENCE pse 
    ON p.PACKAGE_SCHEMA_NAME = pse.PACKAGE_SCHEMA_NAME AND p.PACKAGE_NAME = pse.PACKAGE_NAME
WHERE (p.MOST_RECENT_TABLE_USAGE < CURRENT DATE - 3 YEARS
    OR p.MOST_RECENT_TABLE_USAGE IS NULL)
    AND (pse.PROGRAM_IN_USE IS NULL OR pse.PROGRAM_IN_USE != 'Y')

UNION

SELECT 
    PACKAGE_SCHEMA_NAME,
    PACKAGE_NAME,
    NULL as MOST_RECENT_TABLE_USAGE,
    'Explicitly marked as not in use' as STATUS,
    PROGRAM_IN_USE,
    COMMENT,
    USED_BY,
    'Marked as not in use (PROGRAM_IN_USE = ''N'')' as DETECTION_METHOD,
    1 as SORT_NULL_FIRST,
    DATE('9999-12-31') as SORT_DATE
FROM DBM.DBQA_PACKAGE_SOURCE_EXISTENCE
WHERE PROGRAM_IN_USE = 'N';

COMMENT ON TABLE TV.V_UNUSED_PACKAGES_ANALYSIS IS 'Analyzes package usage patterns based on table access history and explicit usage flags to identify potentially unused packages';
GRANT SELECT ON TV.V_UNUSED_PACKAGES_ANALYSIS TO PUBLIC;
GRANT CONTROL ON TABLE TV.V_UNUSED_PACKAGES_ANALYSIS TO FKGEISTA;
GRANT CONTROL ON TABLE TV.V_UNUSED_PACKAGES_ANALYSIS TO FKSVEERI;
COMMIT;

-- =============================================
-- Statement Validation Views
-- =============================================
-- Views for validating SQL statement consistency
CREATE OR REPLACE VIEW tv.V_DBQA_STATEMENT_PARTS_HASH_CHK AS
    SELECT dsp.STATEMENT_HASH,
       COUNT(DISTINCT dsp.STATEMENT_TYPE) AS Unique_Statement_Types,
       COUNT(DISTINCT dsp.STATEMENT_SUBTYPE) AS Unique_Statement_Subtypes,
       COUNT(DISTINCT dsp.STATEMENT_CATEGORY) AS Unique_Statement_Categories,
       COUNT(DISTINCT dsp.STATEMENT_TEXT) AS Unique_Statement_Texts
FROM dbm.DBQA_STATEMENT_PARTS dsp
JOIN dbm.DBQA_SQL_ANALYSIS dsa
ON dsp.DBQA_SQL_ANALYSIS_ID = dsa.DBQA_SQL_ANALYSIS_ID
WHERE dsa.APPLICATION_NAME = 'FKM'
GROUP BY dsp.STATEMENT_HASH
HAVING COUNT(DISTINCT dsp.STATEMENT_TYPE) > 1
    OR COUNT(DISTINCT dsp.STATEMENT_SUBTYPE) > 1
    OR COUNT(DISTINCT dsp.STATEMENT_CATEGORY) > 1
    OR COUNT(DISTINCT dsp.STATEMENT_TEXT) > 1;

COMMENT ON TABLE TV.V_DBQA_STATEMENT_PARTS_HASH_CHK IS 'Validates statement hash consistency by identifying cases where identical hashes have different statement characteristics';
GRANT SELECT ON TV.V_DBQA_STATEMENT_PARTS_HASH_CHK TO PUBLIC;
GRANT CONTROL ON TABLE TV.V_DBQA_STATEMENT_PARTS_HASH_CHK TO FKGEISTA;
GRANT CONTROL ON TABLE TV.V_DBQA_STATEMENT_PARTS_HASH_CHK TO FKSVEERI;
COMMIT;

-- =============================================
-- Source Code Comparison Views
-- =============================================
-- Views for comparing source code across environments
CREATE OR REPLACE VIEW tv.V_DBQA_SOURCE_DIFFERENCES AS
SELECT  
    x.APPLICATION_NAME, 
    x.OBJECT_NAME, 
    x.OBJECT_TYPE,    
    LISTAGG(DSA.ENVIRONMENT_NAME, ', ') WITHIN GROUP (ORDER BY DSA.ENVIRONMENT_NAME) AS ENVIRONMENTS,
    LISTAGG(DSA.LAST_DDL_TIMESTAMP, ', ') WITHIN GROUP (ORDER BY DSA.ENVIRONMENT_NAME) AS LAST_DDL_TIMESTAMPS,
    LISTAGG(DSA.LAST_DDL_USER_ID, ', ') WITHIN GROUP (ORDER BY DSA.ENVIRONMENT_NAME) AS LAST_DDL_USER_IDS
FROM 
    DBM.DBQA_SQL_ANALYSIS DSA
JOIN 
    (SELECT DISTINCT DSA.APPLICATION_NAME, 
        DSA.OBJECT_NAME, 
        DSA.OBJECT_TYPE
    FROM
        (SELECT 
            DSA.APPLICATION_NAME, 
            DSA.OBJECT_NAME, 
            DSA.OBJECT_TYPE,
            DSP.STATEMENT_TYPE,
            DSP.STATEMENT_SUBTYPE,
            DSP.STATEMENT_CATEGORY,
            COUNT(DISTINCT DSP.STATEMENT_HASH) AS distinct_statement_hashes
        FROM 
            DBM.DBQA_STATEMENT_PARTS DSP
        JOIN 
            DBM.DBQA_SQL_ANALYSIS DSA
        ON 
            DSP.DBQA_SQL_ANALYSIS_ID = DSA.DBQA_SQL_ANALYSIS_ID
        WHERE 
            DSA.APPLICATION_NAME = 'FKM'
            AND DSA.OBJECT_TYPE <> 'BIND.SQL'
        GROUP BY 
            DSA.APPLICATION_NAME, 
            DSA.OBJECT_NAME, 
            DSA.OBJECT_TYPE, 
            DSP.STATEMENT_TYPE,
            DSP.STATEMENT_SUBTYPE,
            DSP.STATEMENT_CATEGORY
        HAVING 
            COUNT(DISTINCT DSP.STATEMENT_HASH) > 1
        ) DSA
    ) x
ON 
    DSA.APPLICATION_NAME = x.APPLICATION_NAME
    AND DSA.OBJECT_NAME = x.OBJECT_NAME
    AND DSA.OBJECT_TYPE = x.OBJECT_TYPE
WHERE 
    DSA.APPLICATION_NAME = 'FKM'
    AND DSA.OBJECT_TYPE <> 'BIND.SQL'    
GROUP BY
    x.APPLICATION_NAME, 
    x.OBJECT_NAME, 
    x.OBJECT_TYPE;

