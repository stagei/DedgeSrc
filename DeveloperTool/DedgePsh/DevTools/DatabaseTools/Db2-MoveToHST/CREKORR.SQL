CREATE TABLE DBM.KORRIGERINGER (
  AAR                DECIMAL     (4,0)     NOT NULL WITH DEFAULT 0.0,
  AVDNR              DECIMAL     (6,0)     NOT NULL WITH DEFAULT 0.0,
  KORRNR             DECIMAL     (5,0)     NOT NULL WITH DEFAULT 0.0,
  KORRDATO           DECIMAL     (8,0)     NOT NULL WITH DEFAULT 0.0,
  REF                CHARACTER   (8)       NOT NULL WITH DEFAULT ' ',
  LINJENR            DECIMAL     (3,0)     NOT NULL WITH DEFAULT 0.0,
  FKNR               DECIMAL     (8,0)     NOT NULL WITH DEFAULT 0.0,
  LAGERSTED          DECIMAL     (7,0)     NOT NULL WITH DEFAULT 0.0,
  LEVVARENR          CHARACTER   (15)      NOT NULL WITH DEFAULT ' ',
  VARENAVN           CHARACTER   (24)      NOT NULL WITH DEFAULT ' ',
  KOSTPRIS           DECIMAL     (9,2)     NOT NULL WITH DEFAULT 0.0,
  AVVIK              DECIMAL     (13,3)    NOT NULL WITH DEFAULT 0.0,
  NYBEHOLDNING       DECIMAL     (13,3)    NOT NULL WITH DEFAULT 0.0,
  ARSAK              CHARACTER   (1)       NOT NULL WITH DEFAULT ' ',
  KOMMENTAR          VARCHAR     (100)     NOT NULL WITH DEFAULT '',
  TIDSPUNKT          TIMESTAMP             NOT NULL WITH DEFAULT CURRENT TIMESTAMP,
  ARSAK_OPT          CHARACTER   (2)       NOT NULL WITH DEFAULT ' ',
  PARTINR            CHARACTER   (10)      NOT NULL WITH DEFAULT ' ',
    PRIMARY KEY(aar, AVDNR, KORRNR, LINJENR)
  )  ;
CREATE INDEX DBM.KORR_DATO          ON DBM.KORRIGERINGER
  (AVDNR , FKNR , KORRDATO );
